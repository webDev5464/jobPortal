const mongoose = require('mongoose')
let regUserSchema = mongoose.Schema({
    fullName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    phoneNumber: {
        type: String,
        required: true,
        unique: true
    },
})
let regUserModel = mongoose.model('users', regUserSchema)

module.exports = regUserModel